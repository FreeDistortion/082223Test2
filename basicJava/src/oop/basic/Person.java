package oop.basic;
//클래스를 정의하고 멤버변수를 정의하는 방법
public class Person {
	//클래스의 특성을 나타내는 데이터를 정의 - field or 멤버변수
	public String name;//public	-	같은 패키지 다른 패키지의 모든 class에서 사용 가
	String addr;//default		-	같은 패키지의 class에서만 사용 가능
	private int age;//private	-	선언된 class에서만 사용 가능
//					이와 같은 정보 숨기기 과정을 information hiding이라고 한다
	
}